    //procedimiento (void)
    public void mostrarCosto()
    {
        String articulo=cboArticulos.getSelectedItem().toString();
        switch (articulo) {
            case "USB": lblCosto.setText("S/50.0"); break;
            case "MOUSE": lblCosto.setText("S/75.0"); break;
            case "WEBCAM": lblCosto.setText("S/200.0"); break;
            case "KEYBOARD": lblCosto.setText("S/100.0"); break;
            default:
                throw new AssertionError();
        }
    }
    
    //procedimiento
    public void calcularBruto(int cantidad, double costo)
    {
        bruto=costo*cantidad;
    }
    
    //procedimiento
    public void calcularIgv(double bruto)
    {
        igv=0.19*bruto;
    }
    
    //procedimiento
    public void calcularPropina(double bruto)
    {
        propina=0.01*bruto;
    }
    
    //procedimiento
    public void calcularDescuento(double bruto, int cantidad)
    {
            
        if (cantidad>100) {
            descuento=0.10*bruto;
        }
    }
    
    public void calcularNeto()
    {
        neto=bruto+igv+propina-descuento;
    }
    
            

